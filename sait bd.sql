DROP TABLE IF EXISTS `статистика_магазинов`;

CREATE ALGORITHM=UNDEFINED DEFINER=`root`@`localhost` SQL SECURITY DEFINER VIEW `статистика_магазинов`  AS SELECT `м`.`название` AS `магазин`, count(`з`.`ид`) AS `всего_заданий`, sum(case when `з`.`статус` = 'новый' then 1 else 0 end) AS `новых`, sum(case when `з`.`статус` = 'в_работе' then 1 else 0 end) AS `в_работе`, sum(case when `з`.`статус` = 'выполнен' then 1 else 0 end) AS `выполнено`, sum(case when `з`.`статус` = 'просрочено' and `з`.`дата_окончания` < curdate() then 1 else 0 end) AS `просрочено` FROM (`магазины` `м` left join `задания` `з` on(`м`.`ид` = `з`.`магазин_ид`)) GROUP BY `м`.`ид`, `м`.`название` ;

