-- Структура для представления `задания_полное`
--
DROP TABLE IF EXISTS `задания_полное`;

CREATE ALGORITHM=UNDEFINED DEFINER=`root`@`localhost` SQL SECURITY DEFINER VIEW `задания_полное`  AS SELECT `з`.`ид` AS `ид`, `з`.`название` AS `название`, `з`.`описание` AS `описание`, `м`.`название` AS `магазин`, `з`.`дата_начала` AS `дата_начала`, `з`.`дата_окончания` AS `дата_окончания`, `з`.`дата_факт_выполнения` AS `дата_факт_выполнения`, `з`.`приоритет` AS `приоритет`, `з`.`статус` AS `статус`, `п`.`фио` AS `создал`, to_days(`з`.`дата_окончания`) - to_days(curdate()) AS `дней_до_срока` FROM ((`задания` `з` join `магазины` `м` on(`з`.`магазин_ид` = `м`.`ид`)) join `пользователи` `п` on(`з`.`создал_ид` = `п`.`ид`)) ;

