CREATE ALGORITHM=UNDEFINED DEFINER=`root`@`localhost` SQL SECURITY DEFINER VIEW `специалисты_с_навыками`  AS SELECT `с`.`ид` AS `ид`, `с`.`фио` AS `фио`, `с`.`телефон` AS `телефон`, `с`.`доступен` AS `доступен`, group_concat(`н`.`название` separator ', ') AS `навыки` FROM ((`специалисты` `с` left join `навыки_специалистов` `нс` on(`с`.`ид` = `нс`.`специалист_ид`)) left join `навыки` `н` on(`нс`.`навык_ид` = `н`.`ид`)) GROUP BY `с`.`ид`, `с`.`фио`, `с`.`телефон`, `с`.`доступен` ;

